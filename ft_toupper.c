#include "libft.h"

int toupper (char *str)
{
    int i;
    i   = 0;

    while (str[i])
    {
        if (str[i] >= 'a' && str[i] <= 'z')
        {
            str[i] -= 32;
        }
        i++;
    }
    return (0);
}